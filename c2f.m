% CONVERT DEGREES CELSIUS TO FAHRENHEIT
%----------------------------------------------
% Aerospace Engineering, UT Austin 2016
% Author: Calvin Giddens
%----------------------------------------------
% Variables:
%   c:          Temperature (Celsius)
%
% Output:
%   f:          Temperature (Fahrenheit)
%----------------------------------------------

function f = c2f(c)

    f = (c * (9 / 5)) + 32;
    
end